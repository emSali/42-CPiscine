#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (*s != '\0')
	{
		s++;
	}
	while (i >= 0)
	{
		if (*s == c)
			return ((char *) s);
		i--;
		s--;
	}
	return (NULL);
}
