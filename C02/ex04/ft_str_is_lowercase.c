int	ft_str_is_lowercase(char *str)
{
	int	is_lo;

	is_lo = 1;
	while (*str != '\0')
	{
		if (*str < 97 || *str > 122)
		{
			is_lo = 0;
		}
		str++;
	}
	return (is_lo);
}
